package com.example.geektrust.entities;

public class Location {

    private Integer x;
    private Integer y;
    public Location(Integer x,Integer y)
    {
        this.x=x;
        this.y=y;
    }
    public Integer getX()
    {
        return x;
    }
     public Integer getY()
    {
        return y;
    }

    
}
